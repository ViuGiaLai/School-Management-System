const bcrypt = require('bcrypt');
const Teacher = require('../models/teacherSchema.js');
const Subject = require('../models/subjectSchema.js');

const teacherRegister = async (req, res) => {
    const { name, email, password, role, school, teachSubject, teachSclass } = req.body;
    try {
        // Make sure 'school' is a valid ObjectId string
        if (!school) {
            return res.status(400).json({ message: 'School ID is required' });
        }
        const salt = await bcrypt.genSalt(10);
        const hashedPass = await bcrypt.hash(password, salt);

        const teacher = new Teacher({ name, email, password: hashedPass, role, school, teachSubject, teachSclass });

        const existingTeacherByEmail = await Teacher.findOne({ email });

        if (existingTeacherByEmail) {
            res.send({ message: 'Email already exists' });
        }
        else {
            let result = await teacher.save();
            await Subject.findByIdAndUpdate(teachSubject, { teacher: teacher._id });
            result.password = undefined;
            res.send(result);
        }
    } catch (err) {
        res.status(500).json(err);
    }
};

const teacherLogIn = async (req, res) => {
    try {
        let teacher = await Teacher.findOne({ email: req.body.email });
        if (teacher) {
            const validated = await bcrypt.compare(req.body.password, teacher.password);
            if (validated) {
                if (teacher.teachSubject) {
                    await teacher.populate("teachSubject", "subName sessions");
                }
                if (teacher.school) {
                    await teacher.populate("school", "_id schoolName");
                }
                if (teacher.teachSclass) {
                    await teacher.populate("teachSclass", "sclassName");
                }
                teacher.password = undefined;
                res.send(teacher);
            } else {
                res.send({ message: "Invalid password" });
            }
        } else {
            res.send({ message: "Teacher not found" });
        }
    } catch (err) {
        res.status(500).json(err);
    }
};

const getTeachers = async (req, res) => {
    try {
        // Validate that req.params.id is a valid ObjectId (24 hex chars)
        if (!req.params.id || !/^[a-fA-F0-9]{24}$/.test(req.params.id)) {
            return res.status(400).json({ message: "Invalid school ID" });
        }

        // Ensure all Teacher documents have a valid ObjectId in the 'school' field
        let teachers = await Teacher.find({ school: req.params.id })
            .populate("teachSubject", "subName sessions")
            .populate("teachSclass", "sclassName");

        // Defensive: If teachers is null, set to []
        if (!teachers) teachers = [];

        // Remove password field from each teacher
        let modifiedTeachers = teachers.map((teacher) => {
            const t = { ...teacher._doc };
            delete t.password;
            return t;
        });

        res.send(modifiedTeachers);
    } catch (err) {
        console.error("Error in getTeachers:", err);
        res.status(500).json({ message: "Internal Server Error", error: err.message });
    }
};

